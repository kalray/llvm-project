void KVXTargetInfo::adjust(DiagnosticsEngine &Diags, LangOptions &Opts) {
  TargetInfo::adjust(Diags, Opts);

  // Keep vector aligned to its size.
  if (Opts.OpenCL)
    MaxVectorAlign = 0;
  Opts.FakeAddressSpaceMap |= Opts.OpenCL;
}

